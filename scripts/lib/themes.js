/**
 * Look up a theme by name.
 * Falls back to 'snow' if the name is not found.
 */
function getTheme(name) {
  const key = (name || 'snow').toLowerCase().trim();
  return themes[key] || themes.snow;
}

/**
 * Displayable list of all themes for selection UIs.
 */
const themeList = Object.entries(themes).map(([name, t]) => ({
  name,
  label: t.label,
  description: t.description,
}));

module.exports = { themes, getTheme, themeList };
